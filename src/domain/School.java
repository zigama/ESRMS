package domain;

public class School {
	
	protected String schoolStateCode;
	protected String schoolName;
	protected String schoolShortName;
	protected String schoolBP;
	protected String schoolTelFixed;
	protected String schoolTelMob;
	protected String schoolFax;
	protected String schoolEmail;
	protected String schoolWebSite;
	public String getSchoolStateCode() {
		return schoolStateCode;
	}
	public void setSchoolStateCode(String schoolStateCode) {
		this.schoolStateCode = schoolStateCode;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getSchoolShortName() {
		return schoolShortName;
	}
	public void setSchoolShortName(String schoolShortName) {
		this.schoolShortName = schoolShortName;
	}
	public String getSchoolBP() {
		return schoolBP;
	}
	public void setSchoolBP(String schoolBP) {
		this.schoolBP = schoolBP;
	}
	public String getSchoolTelFixed() {
		return schoolTelFixed;
	}
	public void setSchoolTelFixed(String schoolTelFixed) {
		this.schoolTelFixed = schoolTelFixed;
	}
	public String getSchoolTelMob() {
		return schoolTelMob;
	}
	public void setSchoolTelMob(String schoolTelMob) {
		this.schoolTelMob = schoolTelMob;
	}
	public String getSchoolFax() {
		return schoolFax;
	}
	public void setSchoolFax(String schoolFax) {
		this.schoolFax = schoolFax;
	}
	public String getSchoolEmail() {
		return schoolEmail;
	}
	public void setSchoolEmail(String schoolEmail) {
		this.schoolEmail = schoolEmail;
	}
	public String getSchoolWebSite() {
		return schoolWebSite;
	}
	public void setSchoolWebSite(String schoolWebSite) {
		this.schoolWebSite = schoolWebSite;
	}
	
	
}
